
//An exception class that extends Exception 
public class BadNumberException extends Exception{
	
	public BadNumberException()
	{
		System.out.println("Error! Bad Number");
	}
	public BadNumberException(String e)
	{
		super(e);
	}
	public BadNumberException(Throwable cause)
	{
		super(cause);
	}
	
	

}
